function fr(n: number | string): bigint {
  return bn254_Fr.create(typeof n === 'string' ? BigInt(n) : BigInt(n));
}

function expectPermutation(
  perm: (s: readonly bigint[]) => bigint[],
  input: bigint[],
  expectedStrs: string[]
) {
  const got = perm(input);
  const expected = expectedStrs.map((s) => fr(s));
  expect(got).toEqual(expected);
}

